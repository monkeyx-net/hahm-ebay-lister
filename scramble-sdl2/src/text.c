#define FONT_COUNT (int)(sizeof(FONT) / sizeof(FONT[0]))

static const Glyph *find_glyph(char ch) {
    ch = (char)toupper((unsigned char)ch);
    for (int i = 0; i < FONT_COUNT; i++)
        if (FONT[i].ch == ch) return &FONT[i];
    return &FONT[0]; // space for anything unknown
}

int text_width(const char *str, int scale) {
    int len = (int)strlen(str);
    return len * 6 * scale; // 5px glyph + 1px gap
}

void text_draw(SDL_Renderer *r, int x, int y, int scale, SDL_Color color, const char *str) {
    SDL_SetRenderDrawColor(r, color.r, color.g, color.b, color.a);
    int cx = x;
    for (const char *p = str; *p; p++) {
        const Glyph *g = find_glyph(*p);
        for (int row = 0; row < 7; row++) {
            for (int col = 0; col < 5; col++) {
                if (g->rows[row] & (1 << (4 - col))) {
                    SDL_Rect px = { cx + col * scale, y + row * scale, scale, scale };
                    SDL_RenderFillRect(r, &px);
                }
            }
        }
        cx += 6 * scale;
    }
}
